# konversi ke int
a = int(7)        # hasilnya 7
b = int(9.6)      # hasilnya 9
c = int("12")     # hasilnya 12

print(a)
print(b)
print(c)

# konversi ke float
a = float(5)        # hasilnya 5.0
b = float(6.25)     # hasilnya 6.25
c = float("8")      # hasilnya 8.0
d = float("3.14")   # hasilnya 3.14

print(a)
print(b)
print(c)
print(d)

# konversi ke string
a = str("halo")   # tetap teks
b = str(25)       # jadi "25"
c = str(7.5)      # jadi "7.5"

print(a)
print(b)
print(c)
