# contoh tipe data angka
a = 10      # int
b = 4.5     # float
c = 2j      # complex

print(type(a))
print(type(b))
print(type(c))

# contoh angka bulat (bebas besar/kecil)
a = 5
b = 987654321012345
c = -1200

print(type(a))
print(type(b))
print(type(c))

# contoh angka desimal
a = 2.75
b = 0.0
c = -18.25

print(type(a))
print(type(b))
print(type(c))

# contoh angka dengan notasi ilmiah
a = 5e2
b = 7E3
c = -9.1e50

print(type(a))
print(type(b))
print(type(c))

# contoh bilangan kompleks
a = 4+2j
b = 7j
c = -3j

print(type(a))
print(type(b))
print(type(c))


